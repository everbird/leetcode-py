class Solution:
    # @param {integer[]} nums
    # @param {integer} target
    # @return {boolean}
    def search(self, nums, target):
        b = 0
        e = len(nums) - 1
        while b <= e:
            mid = (b + e) // 2
            if nums[mid] == target:
                return True

            if nums[b] < nums[mid]:
                if nums[b] <= target < nums[mid]:
                    e = mid - 1
                else:
                    b = mid + 1
            elif nums[b] > nums[mid]:
                if nums[mid] < target <= nums[e]:
                    b = mid + 1
                else:
                    e = mid - 1
            else:
                b += 1

        return False


